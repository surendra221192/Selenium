package java_weekend_batch;

public class multioperators_class {
int a,b,add,sub,mul,div;
void add()
{
	add=a+b;
	System.out.println("a="+a+"b="+b+"addition="+add);
}
void sub()
{
	sub=a-b;
	System.out.println("a="+a+"b="+b+"substraction="+sub);
	
}
void mul()
{
	mul=a*b;
	System.out.println("a="+a+"b="+b+"multiplication="+mul);
	
}
void div()
{
	div=a/b;
	System.out.println("a="+a+"b="+b+"division="+div);
	
}


}
